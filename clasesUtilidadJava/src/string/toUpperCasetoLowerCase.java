
package string;

public class toUpperCasetoLowerCase {
    
    public static void main(String[] args) {
        String cadena = "Hola Mundo";
        System.out.println("El texto normal es: " + cadena);
        System.out.println("El texto en mayúsculas es: " + cadena.toUpperCase());
        System.out.println("El texto en minúsculas es: " + cadena.toLowerCase());
    }
}
